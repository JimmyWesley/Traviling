package model;

public class Retorno {	
	
	public int nr_hotels;
    public String name;
    public String label;
    public String dest_type;
    public String image_url;
    public String city_name;
    public String timezone;
    public int dest_id;
    
	public int getNr_hotels() {
		return nr_hotels;
	}
	public void setNr_hotels(int nr_hotels) {
		this.nr_hotels = nr_hotels;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getDest_type() {
		return dest_type;
	}
	public void setDest_type(String dest_type) {
		this.dest_type = dest_type;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public String getTimezone() {
		return timezone;
	}
	public void setTimezone(String timezone) {
		this.timezone = timezone;
	}
	public int getDest_id() {
		return dest_id;
	}
	public void setDest_id(int dest_id) {
		this.dest_id = dest_id;
	}
    
    
    
    
}